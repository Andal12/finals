# Arithmetic Operators
b = 5
c = 2
addition = b + c
subtraction = b - c
multiplication = b * c
division = b / c
modulus = b % c
exponentiation = b ** c

print("Arithmetic Operators:")
print("Addition:", addition)
print("Subtraction:", subtraction)
print("Multiplication:", multiplication)
print("Division:", division)
print("Modulus:", modulus)
print("Exponentiation:", exponentiation)

# Comparison Operators
x = 12
y = 6
equal = x == y
not_equal = x != y
greater_than = x > y
less_than = x < y
greater_than_or_equal = x >= y
less_than_or_equal = x <= y

print("\nComparison Operators:")
print("Equal:", equal)
print("Not Equal:", not_equal)
print("Greater Than:", greater_than)
print("Less Than:", less_than)
print("Greater Than or Equal:", greater_than_or_equal)
print("Less Than or Equal:", less_than_or_equal)

# Logical Operators
a = True
b = False
and_ta = a and b
or_ta = a or b
not_ta = not a

print("\nLogical Operators:")
print("AND:", and_ta)
print("OR:", or_ta)
print("NOT:", not_ta)

# Assignment Operators
x = 15
y = 7
x += y  # Equivalent to x = x + y
x -= y  # Equivalent to x = x - y
x *= y  # Equivalent to x = x * y
x /= y  # Equivalent to x = x / y

print("\nAssignment Operators:")
print("+=:", x)

# Bitwise Operators
a = 8
b = 5
bitwise_and = a & b
bitwise_or = a | b
bitwise_xor = a ^ b
bitwise_not_a = ~a
left_shift = a << 1
right_shift = a >> 1

print("\nBitwise Operators:")
print("Bitwise AND:", bitwise_and)
print("Bitwise OR:", bitwise_or)
print("Bitwise XOR:", bitwise_xor)
print("Bitwise NOT:", bitwise_not_a)
print("Left Shift:", left_shift)
print("Right Shift:", right_shift)









# Lists: Ordered collection, denoted by square brackets
fruits_list = ['mango', 'banana', 'apple', 'pineapple', 'orange', 'melon']
print("List of fruits:", fruits_list)

fruits_list = ['mango', 'banana', 'apple', 'pineapple', 'orange', 'melon']
fruits_list.append('watermelon')
print(1,fruits_list)

# Tuples: Ordered, immutable collection, denoted by parentheses
fruits_tuple = ('mango', 'banana', 'apple', 'pineapple', 'orange', 'melon')
print("\nTuple of fruits:", fruits_tuple)

# Sets: Unordered, unique collection, denoted by curly braces
fruits_set = {'mango', 'banana', 'apple', 'pineapple', 'orange', 'melon'}
print("\nSet of fruits:", fruits_set)



 
       
